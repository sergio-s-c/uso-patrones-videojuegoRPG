    private void jLabelHogueraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelHogueraMouseClicked
        //Recupera la vida y mana a los personajes del equipo con 1 clic y al segundo clic te lleva a partida
        if (numClic == 0) {
            for (int i = 0; i < equipo.size(); i++) {//regeneramos el mana tras la pelea
                equipo.get(i).setSalud(equipo.get(i).getVidaMaxima());
                equipo.get(i).setMana(equipo.get(i).getManaMaximo());
            }
            jTextField1.setText("Todos los componentes del equipo recuperaron su salud y su manÃ¡");
            numClic++;
        } else {
            
            this.setVisible(false);
            partida.getMapa().setEventosRealizados(partida.getMapa().getEventosRealizados() + 1);
            try {
                partida.actualizar();

            } catch (IOException ex) {
                Logger.getLogger(VentanaCofre.class.getName()).log(Level.SEVERE, null, ex);
            }

            partida.setVisible(true);
        }
    }//GEN-LAST:event_jLabelHogueraMouseClicked

